/*黄金分割数是个无理数，也就是无法表示为两个整数的比值。0.618只是它的近似值，其真值可以通过对 5 开方减去 1 再除以 2 来获得，我们取它的一个较精确的近似值：0.6180340.618034。

有趣的是，一些简单的数列中也会包含这个无理数，这很令数学家震惊！

1\ 3\ 4\ 7\ 11\ 18\ 29\ 47 ...... 称为“鲁卡斯队列”。它后面的每一个项都是前边两项的和。

如果观察前后两项的比值，即：1/3,3/4,4/7,7/11,11/18 .....会发现它越来越接近于黄金分割数！

你的任务就是计算出从哪一项开始，这个比值四舍五入后已经达到了与 0.618034 一致的精度。

请写出该比值。格式是：分子/分母。比如：29/4729/47。*/

#include <iostream>
using namespace std;
int main()
{
  float a = 1,b = 3,c = 0;
  float n = 0;
  while(1){
    n = a/b;
    if((n>=0.6180335)&&(n<=0.6180344))
      break;
    c = a + b;
    a = b;
    b = c;
  }
  cout<<a<<'/'<<b;;
  return 0;
}
