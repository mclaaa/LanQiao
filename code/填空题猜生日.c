/*本题为填空题，只需要算出结果后，在代码中使用输出语句将所填结果输出即可。

今年的植树节（2012 年 3 月 1212 日），小明和他的叔叔还有小伙伴们一起去植树。休息的时候，小明的同学问他叔叔多大年纪，他叔叔说：“我说个题目，看你们谁先猜出来！”

“把我出生的年月日连起来拼成一个 8位数（月、日不足两位前补 0）正好可以被今天的年、月、日整除！”

他想了想，又补充到：“再给个提示，我是 6 月出生的。”

根据这些信息，请你帮小明算一下，他叔叔的出生年月日。

格式是年月日连成的 8 位数。例如，如果是 1948 年 6 月 12 日，就写：19480612。*/

#include <stdlib.h>
#include <stdio.h>

int main()
{
  int year,day,s;
  for(year=1950;year<2012;year++)
    for(day=1;day<30;day++)
  {
        s=10000*year+100*6+day;
        if(s%2012==0 && s%3==0 && s%12==0)

        {
            printf("%d",s);
            break;
        }
      }
  return 0;
}
